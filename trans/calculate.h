#ifndef CALCULATE_H
#define CALCULATE_H

/**
 * Calculates the vector magnitude of three axes.
 * @param a the first axis.
 * @param b the second axis.
 * @param c the third axis.
 * @return the sqrt of the sum of the squares of a, b, and c.
 */
double magnitude(long long a, long long b, long long c);

#endif /* CALCULATE_H */

